package com.dl.one;

public class Eg1 {

	public static void main(String[] args) {
		
		//integer Literal
		int a = 10;
		System.out.println(a);
		
		//int is a data type
		//a is a variable
		//= is operator
		//10 is constant value
		//; end of line
		
		//Floating Literal
		float f1= 10.0f;
		//for float prefix should be written. the prefix is F or f
		System.out.println(f1);
		
		double d1= 20.0d;
		System.out.println(d1);
		
	    //Boolean Literals
		boolean b1 = true;
		System.out.println(b1);
		
		//Character Literal
		char ch = 'A';
		System.out.println(ch);
		
		//string Literals
		String name = "Chandana";
		System.out.println(name);
		
		//Binary Literals
		int x = 0b010;
		System.out.println(x);
		
		//Octal Literals
		int y = 01234;
		System.out.println(y);
		
		//HexaDecimal Literal
		int z =  0XEC12;
		System.out.println(z);

	}

}
